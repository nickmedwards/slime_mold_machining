# some nice visual stuff needs updating bc changed grow's return
def save_gif():
    fps = 1
    mold_growth = [np.copy(slime.mold)]
    while slime.grow(petri.dish): mold_growth += [np.copy(slime.mold)]
    nSeconds = len(mold_growth)

    # First set up the figure, the axis, and the plot element we want to animate
    fig = plt.figure( figsize=(8,8) )


    mold_on_dish = lambda mold: petri.dish + mold * -petri.dish
    # first image
    a = mold_on_dish(mold_growth[0])
    im = plt.imshow(a, interpolation='none', aspect='auto')

    def animate_func(i):
        if i % fps == 0: print( '.', end ='' )
        im.set_array(mold_on_dish(mold_growth[i]))
        return [im]

    # create and save animation
    anim = animation.FuncAnimation(fig, animate_func, frames = nSeconds * fps, interval = 1000 / fps,)
    anim.save('mold_growth.gif', fps=fps)

    print('\nsaved growth animation at mold_growth.gif')

